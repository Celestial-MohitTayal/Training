//If...Else Statement:
            let age = 18;
            if (age >= 18) {
              console.log("You are an adult.");
            } else {
              console.log("You are a minor.");
            }

//Switch Statement:
            let day = "Monday";
            switch (day) {
              case "Monday":
                console.log("Start of the workweek.");
                break;
              case "Friday":
                console.log("End of the workweek.");
                break;
              default:
                console.log("Midweek day.");
            }